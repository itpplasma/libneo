        call print_test("test_bessel_kn_recurrence_overflow")

        k = bessel_kn(200, 0.5d0)
        if (.not. (k > huge(1.0d0))) then
            print *, "expected +Inf, got", k
            call print_fail
            stop 1
        end if

        call print_ok
